public class Vampire {
    private int x; //x coordinate
    private int y; //y coordinate
    
    public Vampire(){
        
    }
    
    public void setXY(int x, int y){
        this.x = x;
        this.y = y;
    }
    
    public int getX(){
        return this.x;
    }
    
    public int getY(){
        return this.y;
    }
}
